export function getBestFor(useCase: UseCase, models: LLMModel[] = MODELS): LLMModel {
  const fieldMap: Record<UseCase, keyof LLMModel> = {
    coding: 'coding',
    writing: 'writing',
    math: 'math',
    vision: 'quality',
    local: 'speed',
    'low-end': 'speed',
    reasoning: 'reasoning',
    chat: 'quality',
    multilingual: 'quality',
    research: 'quality',
    chinese: 'quality',
    korean: 'quality',
    english: 'quality',
    agents: 'quality',
  };
  const field = fieldMap[useCase];
  return [...models].sort((a, b) => (b[field] as number) - (a[field] as number))[0];
}

export function getTopN(n: number, field: keyof LLMModel = 'rating', models: LLMModel[] = MODELS): LLMModel[] {
  return [...models]
    .sort((a, b) => (b[field] as number) - (a[field] as number))
    .slice(0, n);
}

export function formatContext(tokens: number): string {
  if (tokens >= 1_000_000) return `${(tokens / 1_000_000).toFixed(1)}M`;
  if (tokens >= 1_000) return `${(tokens / 1_000).toFixed(0)}K`;
  return tokens.toString();
}

export function formatPrice(model: LLMModel): string {
  if (!model.pricePerMillion) return 'Open Source';
  const { input, output } = model.pricePerMillion;
  return `$${input.toFixed(2)} / $${output.toFixed(2)}`;
}
