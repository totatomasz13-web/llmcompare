function formatParamSize(size?: number): string | undefined {
  if (size === undefined || size === null) return undefined;
  if (size >= 1_000_000_000_000) return `${(size / 1_000_000_000_000).toFixed(1)}T`;
  if (size >= 1_000_000_000) return `${(size / 1_000_000_000).toFixed(0)}B`;
  if (size >= 1_000_000) return `${(size / 1_000_000).toFixed(0)}M`;
  if (size >= 1_000) return `${(size / 1_000).toFixed(0)}K`;
  return size.toString();
}

function splitSentences(text: string): string[] {
  if (!text) return [];
  return text
    .split(/[.;]\s+/)
    .map((s) => s.trim())
    .filter(Boolean);
}

